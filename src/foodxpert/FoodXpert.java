
package foodxpert;

public class FoodXpert {
    String fi1, fi2, fi3, fi4, fi5;
         
    FoodXpert(String x, String p, String e, String r, String t){
        fi1 = x;
        fi2 = p;
        fi3 = e;
        fi4 = r;
        fi5 = t;
    }
    
    void FoodXpert_show(){
        System.out.println(fi1);
        System.out.println(fi2);
        System.out.println(fi3);
        System.out.println(fi4);
        System.out.println(fi5);
        System.out.println("\n\n");
    }
    
}
